// ========== UTILITY FUNCTIONS ==========

function generateId(prefix, list) {
    const num = list.length + 1;
    return `${prefix}-${String(num).padStart(3, '0')}`;
}

// Generate 12-digit ID: CCCCYYXXXXXX
// CCCC = Transportation Planning Point (BKK=1101, MTP=1102)
// YY = last 2 digits of year
// XXXXXX = running number 6 digits
function generateTugId(site, list) {
    const cccc = site === 'BKK' ? '1101' : '1102';
    const yy = String(new Date(nowBKK()).getFullYear()).slice(-2);
    const num = list.length + 1;
    return `${cccc}${yy}${String(num).padStart(6, '0')}`;
}

function generateShipmentId(site, list) {
    const cccc = site === 'BKK' ? '1101' : '1102';
    const yy = String(new Date(nowBKK()).getFullYear()).slice(-2);
    const num = list.length + 1;
    return `${cccc}${yy}${String(num).padStart(6, '0')}`;
}

// Return current date/time in GMT+7 as ISO-like string
function nowBKK() {
    const d = new Date(Date.now() + 7 * 3600000);
    return d.toISOString().slice(0, 19); // yyyy-MM-ddTHH:mm:ss in GMT+7
}
function todayBKK() { return nowBKK().slice(0, 10); }
function nowBKK16() { return nowBKK().slice(0, 16); }

function formatDateTime(dt) {
    if (!dt) return '-';
    if (dt === 'skipped') return t('skipped');
    const d = new Date(dt);
    if (isNaN(d.getTime())) return '-';
    return d.toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric', timeZone: 'Asia/Bangkok' }) + ' ' +
           d.toLocaleTimeString('en-GB', { hour: '2-digit', minute: '2-digit', timeZone: 'Asia/Bangkok' });
}

function formatDate(dt) {
    if (!dt) return '-';
    return new Date(dt).toLocaleDateString('en-GB', { day: '2-digit', month: 'short', year: 'numeric', timeZone: 'Asia/Bangkok' });
}

function showToast(message, type = '') {
    const existing = document.querySelector('.toast');
    if (existing) existing.remove();
    const toast = document.createElement('div');
    toast.className = `toast ${type}`;
    toast.textContent = message;
    document.body.appendChild(toast);
    setTimeout(() => toast.remove(), 3000);
}

