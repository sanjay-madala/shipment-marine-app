// Helper: build closed reportIns with Start→Work→Last pattern
// aS/aW/aE = actual start/workStart/end; bS/bW/bE = billing start/workStart/end
function _mkRI(aS, aW, aE, bS, bW, bE) {
    const sk = { startTime: 'skipped', endTime: 'skipped' };
    function stages(S, W, E, billing) {
        const ov = billing ? { overrideStart: false, overrideEnd: false } : {};
        return [
            { name: 'Start',          startTime: S,  endTime: S,  required: true,  remark: '', ...ov },
            { name: 'Stand by #1',   ...sk,           required: false, remark: '', ...ov },
            { name: 'Work Period #1', startTime: W,  endTime: E,  required: false, remark: '', ...ov },
            { name: 'Stand by #2',   ...sk,           required: false, remark: '', ...ov },
            { name: 'Work Period #2',...sk,            required: false, skippable: true, remark: '', ...ov },
            { name: 'Stand by #3',   ...sk,           required: false, remark: '', ...ov },
            { name: 'Work Period #3',...sk,            required: false, skippable: true, remark: '', ...ov },
            { name: 'Last',           startTime: E,  endTime: E,  required: true,  remark: '', ...ov },
        ];
    }
    return {
        actual:  { stages: stages(aS, aW, aE, false), confirmedAt: aE },
        billing: { stages: stages(bS, bW, bE, true),  confirmedAt: bE },
    };
}

