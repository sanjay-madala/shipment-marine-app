// Lookup WBS by tug ID and job type
function lookupWBS(tugId, jobTypeId) {
    // Try exact job type first, then fallback to HO
    return WBS_MASTER[`${tugId}_${jobTypeId}`] || WBS_MASTER[`${tugId}_HO`] || '';
}

